public class Pattern2{
	public static void main(String[] args){
		for(int i=1;i<=5;i++){
			for(int j=1;j<=10;j++){
				if(i==1&&j==1){
					System.out.print("*");
				}	
				else if(i==1&&j==6){
					System.out.print("*");
				}
				else if(i==1&&j==10){
					System.out.print("*");
				}
				else if(i==2&&j==2){
					System.out.print("*");
				}
				else if(i==2&&j==7){
					System.out.print("*");
				}
				else if(i==2&&j==9){
					System.out.print("*");
				}
				else if(i==3&&j==3){
					System.out.print("*");
				}
				else if(i==3&&j==8){
					System.out.print("*");
				}
				else if(i==4&&j==4){
					System.out.print("*");
				}
				else if(i==4&&j==7){
					System.out.print("*");
				}
				else if(i==4&&j==9){
					System.out.print("*");
				}
				else if(i==5&&j==5){
					System.out.print("*");
				}
				else if(i==5&&j==6){
					System.out.print("*");
				}
				else if(i==5&&j==10){
					System.out.print("*");
				}
				
				else{
					System.out.print(" ");
				}
			}
				System.out.println();
			
		}
	}
}
	
	
	
	